package com.slyvronline.mysterycolor;

public class ColorDefinition {

	int id;
	String name;
	int rLow;
	int rHigh;
	int gLow;
	int gHigh;
	int bLow;
	int bHigh;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getrLow() {
		return rLow;
	}
	public void setrLow(int rLow) {
		this.rLow = rLow;
	}
	public int getrHigh() {
		return rHigh;
	}
	public void setrHigh(int rHigh) {
		this.rHigh = rHigh;
	}
	public int getgLow() {
		return gLow;
	}
	public void setgLow(int gLow) {
		this.gLow = gLow;
	}
	public int getgHigh() {
		return gHigh;
	}
	public void setgHigh(int gHigh) {
		this.gHigh = gHigh;
	}
	public int getbLow() {
		return bLow;
	}
	public void setbLow(int bLow) {
		this.bLow = bLow;
	}
	public int getbHigh() {
		return bHigh;
	}
	public void setbHigh(int bHigh) {
		this.bHigh = bHigh;
	}
	
	
}
